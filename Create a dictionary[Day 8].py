'''100 Days Python Challenge
Day 8	Python Basics - Dictionaries	Create a dictionary and access values by keys.
'''

# Create a dictionary
person = {
    "name": "Alice",
    "age": 25,
    "city": "New York"
}

# Access values by keys
print(person["name"])  # Output: Alice
print(person["age"])   # Output: 25
print(person["city"])  # Output: New York

print(person)
