''' 100 days Python Challenge Day 2
Basic Arithmetic Operations in Python '''

# Addition
addition = 10 + 5

print("Addition (10 + 5):", addition)

# Subtraction
subtraction = 10 - 5
print("Subtraction (10 - 5):", subtraction)

# Multiplication
multiplication = 10 * 5
print("Multiplication (10 * 5):", multiplication)

# Division
division = 10 / 5
print("Division (10 / 5):", division)

# Modulus (Remainder of division)
modulus = 10 % 3
print("Modulus (10 % 3):", modulus)

# Exponentiation (Power)
exponentiation = 2 ** 3
print("Exponentiation (2 ** 3):", exponentiation)

# Floor Division (Quotient without the remainder)
floor_division = 10 // 3
print("Floor Division (10 // 3):", floor_division)
