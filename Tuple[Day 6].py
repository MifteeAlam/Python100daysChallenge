''' 100 days python challenge
Day 6	Python Basics - Tuples	Create a tuple and demonstrate accessing elements by index.'''


# Creating a tuple
my_tuple = (10, 20, 30, 40, 50)

# Accessing elements by index
first_element = my_tuple[0]   # Accessing the first element
second_element = my_tuple[1]  # Accessing the second element
last_element = my_tuple[-1]   # Accessing the last element using a negative index

# Printing the accessed elements
print("First element:", first_element)
print("Second element:", second_element)
print("Last element:", last_element)



# Printing the whole tuple
print("The entire tuple:", my_tuple)
